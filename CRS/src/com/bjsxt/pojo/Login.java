package com.bjsxt.pojo;

/**
 * 登录信息实体类
 * @author Qiao
 *
 */
public class Login {
	
	private Integer lid;//登录信息编号
	private Integer uid;//管理员Id
	private String ip;//登录的ip地址
	private String time;//登录时间
	
	private User user;

	public Login(Integer lid, Integer uid, String ip, String time, User user) {
		super();
		this.lid = lid;
		this.uid = uid;
		this.ip = ip;
		this.time = time;
		this.user = user;
	}

	public Login() {
		super();
	}

	public Integer getLid() {
		return lid;
	}

	public void setLid(Integer lid) {
		this.lid = lid;
	}

	public Integer getUid() {
		return uid;
	}

	public void setUid(Integer uid) {
		this.uid = uid;
	}

	public String getIp() {
		return ip;
	}

	public void setIp(String ip) {
		this.ip = ip;
	}

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public User getUser() {
		return user;
	}

	public void setUser(User user) {
		this.user = user;
	}

	@Override
	public String toString() {
		return "Login [lid=" + lid + ", uid=" + uid + ", ip=" + ip + ", time=" + time + ", user=" + user + "]";
	}
	
}
