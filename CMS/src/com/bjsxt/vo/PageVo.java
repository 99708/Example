package com.bjsxt.vo;

import java.util.List;

/**
 * 分页实体类
 * @author 997
 *
 */
public class PageVo {
	
	
	private int pageNum;
	private int pageSize;
	private int pageStart;
	/**
	 * 总的记录数
	 */
	private int count;
	/**
	 * 总的页码数
	 */
	private int pageCount;
	/**
	 * 每页的信息
	 */
	private List<StudentScoreVo> ssList;
	public int getPageNum() {
		return pageNum;
	}
	public void setPageNum(int pageNum) {
		this.pageNum = pageNum;
	}
	public int getPageSize() {
		return pageSize;
	}
	public void setPageSize(int pageSize) {
		this.pageSize = pageSize;
	}
	public int getPageStart() {
		return pageStart;
	}
	public void setPageStart(int pageStart) {
		this.pageStart = pageStart;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public int getPageCount() {
		return pageCount;
	}
	public void setPageCount(int pageCount) {
		this.pageCount = pageCount;
	}
	public List<StudentScoreVo> getSsList() {
		return ssList;
	}
	public void setSsList(List<StudentScoreVo> ssList) {
		this.ssList = ssList;
	}
	public PageVo(int pageNum, int pageSize, int pageStart, int count,
			int pageCount, List<StudentScoreVo> ssList) {
		super();
		this.pageNum = pageNum;
		this.pageSize = pageSize;
		this.pageStart = pageStart;
		this.count = count;
		this.pageCount = pageCount;
		this.ssList = ssList;
	}
	public PageVo() {
		super();
	}
	
	
	
}
